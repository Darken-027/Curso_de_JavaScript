//Ejercicio 10: Declarar una funcion usando la instrucion function,

function sumar(a, b){
    var suma = a + b;

    return suma;
}
console.log(sumar(1,1));
//Se puede duplicar la funcion cuantas veces se quiera.
console.log(sumar(1,5));
console.log(sumar(4,8));
console.log(sumar(9,44));

//Las funciones son una evolucion de la ingenieria de software por que permiten encapsular un conjunto de instruciones que se pueden reutilizar (Evita la duplicacion de codigo).